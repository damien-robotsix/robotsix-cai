#!/usr/bin/env bash
#
# robotsix-cai container entrypoint
# ---------------------------------
#
# 1. Template the crontab from env vars. For now there's one task:
#    the analyzer runs on CAI_ANALYZER_SCHEDULE (default: hourly).
#    When we add more tasks (daily token report, workflow-triggered
#    runs, ...) they become additional env vars + crontab lines here.
#
# 2. Do one synchronous `cai.py init` + `cai.py analyze` pass so
#    `docker compose up -d` produces useful logs immediately rather
#    than waiting for the first cron tick.
#
# 3. Exec supercronic as PID 1. supercronic handles SIGTERM gracefully
#    (lets in-flight tasks finish) and streams child stdout/stderr to
#    its own stdout, which docker logs then captures.

set -euo pipefail

CAI_ANALYZER_SCHEDULE="${CAI_ANALYZER_SCHEDULE:-0 * * * *}"
CRONTAB_PATH=/tmp/crontab

cat > "$CRONTAB_PATH" <<CRONTAB
# Generated by entrypoint.sh at container startup.
# Each line is an independent cron entry — add more tasks as new lines.
$CAI_ANALYZER_SCHEDULE python /app/cai.py analyze
CRONTAB

echo "[entrypoint] crontab:"
sed 's/^/[entrypoint]   /' "$CRONTAB_PATH"
echo

echo "[entrypoint] running initial cai.py init"
python /app/cai.py init || echo "[entrypoint] init exited non-zero; continuing"

echo "[entrypoint] running initial cai.py analyze"
python /app/cai.py analyze || echo "[entrypoint] analyze exited non-zero; continuing"

echo "[entrypoint] handing off to supercronic"
exec supercronic "$CRONTAB_PATH"
