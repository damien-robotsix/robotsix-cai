#!/usr/bin/env bash
#
# robotsix-cai container entrypoint
# ---------------------------------
#
# 1. Template the crontab from env vars. Independent tasks:
#      - analyze:   parse own transcripts, raise findings as issues
#      - fix:       pick an eligible issue and let the subagent fix it
#      - refine:    turn human-filed issues into structured plans
#      - review-pr: pre-merge consistency review of open PRs
#      - revise:    iterate on open PRs based on review comments
#      - verify:    walk pr-open issues and update labels per PR state
#      - audit:     periodic queue/PR consistency checks
#      - code-audit:    periodic source code consistency checks
#      - propose:       weekly creative improvement proposals
#      - update-check:  periodic Claude Code release checks
#      - confirm:       verify merged fixes are actually solved
#      - merge:         confidence-gated auto-merge for bot PRs
#    Each is its own crontab line so supercronic runs them as
#    independent processes — natural concurrency, easy to add more.
#
# 2. Do one synchronous pass of init / analyze / refine / fix / review-pr / revise / merge / verify / audit so
#    `docker compose up -d` produces useful logs immediately rather
#    than waiting for the first cron tick.
#
# 3. Exec supercronic as PID 1. supercronic handles SIGTERM gracefully
#    (lets in-flight tasks finish) and streams child stdout/stderr to
#    its own stdout, which docker logs then captures.

set -euo pipefail

CAI_ANALYZER_SCHEDULE="${CAI_ANALYZER_SCHEDULE:-0 0 * * *}"
CAI_FIX_SCHEDULE="${CAI_FIX_SCHEDULE:-15 * * * *}"
CAI_VERIFY_SCHEDULE="${CAI_VERIFY_SCHEDULE:-45 * * * *}"
CAI_AUDIT_SCHEDULE="${CAI_AUDIT_SCHEDULE:-0 */6 * * *}"
CAI_AUDIT_TRIAGE_SCHEDULE="${CAI_AUDIT_TRIAGE_SCHEDULE:-10 */6 * * *}"
CAI_CODE_AUDIT_SCHEDULE="${CAI_CODE_AUDIT_SCHEDULE:-0 3 * * 0}"
CAI_PROPOSE_SCHEDULE="${CAI_PROPOSE_SCHEDULE:-0 4 * * 0}"
CAI_UPDATE_CHECK_SCHEDULE="${CAI_UPDATE_CHECK_SCHEDULE:-0 4 * * 1}"
CAI_REVISE_SCHEDULE="${CAI_REVISE_SCHEDULE:-30 * * * *}"
CAI_CONFIRM_SCHEDULE="${CAI_CONFIRM_SCHEDULE:-0 2 * * *}"
CAI_REVIEW_PR_SCHEDULE="${CAI_REVIEW_PR_SCHEDULE:-20 * * * *}"
CAI_MERGE_SCHEDULE="${CAI_MERGE_SCHEDULE:-35 * * * *}"
CAI_REFINE_SCHEDULE="${CAI_REFINE_SCHEDULE:-10 * * * *}"

CRONTAB_PATH=/tmp/crontab

cat > "$CRONTAB_PATH" <<CRONTAB
# Generated by entrypoint.sh at container startup.
# Each line is an independent cron entry — add more tasks as new lines.
$CAI_ANALYZER_SCHEDULE python /app/cai.py analyze
$CAI_REFINE_SCHEDULE python /app/cai.py refine
$CAI_FIX_SCHEDULE python /app/cai.py fix
$CAI_REVISE_SCHEDULE python /app/cai.py revise
$CAI_VERIFY_SCHEDULE python /app/cai.py verify
$CAI_AUDIT_SCHEDULE python /app/cai.py audit
$CAI_AUDIT_TRIAGE_SCHEDULE python /app/cai.py audit-triage
$CAI_CODE_AUDIT_SCHEDULE python /app/cai.py code-audit
$CAI_PROPOSE_SCHEDULE python /app/cai.py propose
$CAI_UPDATE_CHECK_SCHEDULE python /app/cai.py update-check
$CAI_CONFIRM_SCHEDULE python /app/cai.py confirm
$CAI_REVIEW_PR_SCHEDULE python /app/cai.py review-pr
$CAI_MERGE_SCHEDULE python /app/cai.py merge
CRONTAB

echo "[entrypoint] crontab:"
sed 's/^/[entrypoint]   /' "$CRONTAB_PATH"
echo

# Wire `gh` in as the git credential helper so the fix subagent can
# `git push` over HTTPS using the same token that's in the
# cai_gh_config volume. ~/.gitconfig isn't persisted across
# container restarts, so we re-run this every startup.
if gh auth status >/dev/null 2>&1; then
  echo "[entrypoint] configuring gh as the git credential helper"
  gh auth setup-git
else
  echo "[entrypoint] gh not yet authenticated; skipping git credential setup"
fi

echo "[entrypoint] running initial cai.py init"
python /app/cai.py init || echo "[entrypoint] init exited non-zero; continuing"

echo "[entrypoint] running initial cai.py analyze"
python /app/cai.py analyze || echo "[entrypoint] analyze exited non-zero; continuing"

echo "[entrypoint] running initial cai.py verify"
python /app/cai.py verify || echo "[entrypoint] verify exited non-zero; continuing"

echo "[entrypoint] running initial cai.py refine"
python /app/cai.py refine || echo "[entrypoint] refine exited non-zero; continuing"

echo "[entrypoint] running initial cai.py fix"
python /app/cai.py fix || echo "[entrypoint] fix exited non-zero; continuing"

echo "[entrypoint] running initial cai.py review-pr"
python /app/cai.py review-pr || echo "[entrypoint] review-pr exited non-zero; continuing"

echo "[entrypoint] running initial cai.py revise"
python /app/cai.py revise || echo "[entrypoint] revise exited non-zero; continuing"

echo "[entrypoint] running initial cai.py merge"
python /app/cai.py merge || echo "[entrypoint] merge exited non-zero; continuing"

echo "[entrypoint] running initial cai.py audit"
python /app/cai.py audit || echo "[entrypoint] audit exited non-zero; continuing"

echo "[entrypoint] running initial cai.py audit-triage"
python /app/cai.py audit-triage || echo "[entrypoint] audit-triage exited non-zero; continuing"

echo "[entrypoint] running initial cai.py code-audit"
python /app/cai.py code-audit || echo "[entrypoint] code-audit exited non-zero; continuing"

echo "[entrypoint] running initial cai.py propose"
python /app/cai.py propose || echo "[entrypoint] propose exited non-zero; continuing"

echo "[entrypoint] running initial cai.py update-check"
python /app/cai.py update-check || echo "[entrypoint] update-check exited non-zero; continuing"

echo "[entrypoint] running initial cai.py confirm"
python /app/cai.py confirm || echo "[entrypoint] confirm exited non-zero; continuing"

echo "[entrypoint] handing off to supercronic"
exec supercronic "$CRONTAB_PATH"
